import java.util.Scanner;
class switchvowels{
public static void main(String args[]){
String a="hello";
for (int i=0;i<a.length();i++){
char b=a.charAt(i);
switch(b){
case 'a':
System.out.println("a is there");
break;
case 'e':
System.out.println("e is there");
break;
case 'i':
System.out.println("i is there");
break;
case 'o':
System.out.println("o is there");
break;
case 'u':
System.out.println("u is there");
break;
}
}



}}